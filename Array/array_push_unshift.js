//Array push pop 

//push

let fruits =["apple","mango","grapes"];
console.log(fruits);
fruits.push("banana");
console.log(fruits);


// pop 

fruits.pop();
console.log(fruits);

// Array shift unshift 

// unshift

fruits.unshift("Pineapple" ,"poddeyfruits");
console.log(fruits);

// shift

fruits.shift();
console.log(fruits);